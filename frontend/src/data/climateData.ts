// Función para generar datos de predicción (simulados)
export const generatePredictionData = (baseData: any, year: number) => {
  const currentYear = new Date().getFullYear();
  const yearsDiff = year - currentYear;
  
  if (yearsDiff <= 0) return baseData;
  
  // Simular cambio climático: aumento gradual de temperatura y cambios en precipitación
  return baseData.months.map((month: any) => ({
    ...month,
    temperature: month.temperature + (yearsDiff * 0.1), // Aumento de 0.1°C por año
    precipitation: month.precipitation * (1 + (Math.random() - 0.5) * 0.1 * yearsDiff), // Variación en precipitación
    humidity: Math.max(0, Math.min(100, month.humidity + (Math.random() - 0.5) * 5 * yearsDiff)),
    uvIndex: Math.max(0, Math.min(15, month.uvIndex + yearsDiff * 0.05))
  }));
};